/**
 * IconButton - Button with SVG icon
 */

export interface IconButtonOptions {
  icon: string;
  title: string;
  onClick: () => void;
  className?: string;
  active?: boolean;
}

export class IconButton {
  private element: HTMLButtonElement;
  private options: IconButtonOptions;

  constructor(options: IconButtonOptions) {
    this.options = options;
    this.element = document.createElement('button');
    this.render();
  }

  private render(): void {
    this.element.className = `btn btn-icon ${this.options.className || ''} ${this.options.active ? 'active' : ''}`;
    this.element.title = this.options.title;
    this.element.innerHTML = this.options.icon;
    this.element.onclick = this.options.onClick;
  }

  getElement(): HTMLButtonElement {
    return this.element;
  }

  setActive(active: boolean): void {
    this.options.active = active;
    this.element.classList.toggle('active', active);
  }

  update(options: Partial<IconButtonOptions>): void {
    Object.assign(this.options, options);
    this.render();
  }
}

